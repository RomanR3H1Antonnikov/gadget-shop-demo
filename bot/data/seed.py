async def seed():
    await init_db()

    async with aiosqlite.connect(DB_PATH) as db:
        # Проверяем — если данные уже есть, не дублируем
        async with db.execute("SELECT COUNT(*) FROM categories") as cursor:
            if (await cursor.fetchone())[0] > 0:
                print("БД уже заполнена, пропускаем seed.")
                return

        # Вставляем категории с явными id
        await db.executemany(
            "INSERT INTO categories (id, name, parent_id, emoji, sort_order) VALUES (?, ?, ?, ?, ?)",
            CATEGORIES
        )

        # Вставляем товары
        await db.executemany(
            "INSERT INTO products (category_id, name, description, price) VALUES (?, ?, ?, ?)",
            PRODUCTS
        )

        await db.commit()
        print(f"Добавлено {len(CATEGORIES)} категорий и {len(PRODUCTS)} товаров.")


if __name__ == "__main__":
    asyncio.run(seed())
